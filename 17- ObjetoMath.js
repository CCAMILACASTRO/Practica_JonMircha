let numeroDecimal1 = 1.25;
let numeroDecimal2 = 1.75;

console.log(Math.min(50,12,37,17,25,6)); // Min() = retorna el numero minimo de un listado

console.log(Math.max(50,12,37,17,25,6)); // Max() = retorna el numero maximo de un listado

console.log(Math.ceil(numeroDecimal1)); // ceil()= retorna el numero entero mayor + cercano (redondea arriba)

console.log(Math.floor(numeroDecimal2)); // floor()= retorna el numero entero menor + cercano (redondea abajo)

console.log(Math.round(numeroDecimal1)); // roun()= redondea al entero mas cercano. (0.49 abajo, 0.5 arriba)

console.log(Math.random());  // random()= retorna numero aleatorio entre 0 y 1 o rango que indiquemos.

console.log(Math.round(Math.random()));  /* redondea entre el 0 y 1 un numero aleatorio*/

console.log(Math.round(Math.random()*50))  /*redondea numero entero entre numero aleatorio entre 0 y 50.*/

console.log(Math.round(Math.random()*50 + 20)) /* redondea numero entero entre numero aleatorio entre 20 y 70*/

console.log(Math.pow(2, 3))  // retorna la potencia de un nuemero (base, exponente)

console.log(Math.abs(7.8)); // retorna valor absoluto.

console.log(Math.sqrt(81)); // retorna la raiz cuadrada.

console.log(Math.sign(-7.8));  // indica si el numero es positivo, negativo o 0.





